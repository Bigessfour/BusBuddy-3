#!/usr/bin/env node

/**
 * Test Grok-4 API to analyze BusBuddy MCP file structure
 * Ask for recommendations on proper setup and organization
 */

import fs from "fs/promises";
import path from "path";

async function analyzeMCPStructure() {
    const apiKey = process.env.XAI_API_KEY;

    if (!apiKey) {
        console.error("❌ XAI_API_KEY not set");
        process.exit(1);
    }

    console.log("🧪 Analyzing BusBuddy MCP Structure with Grok-4...");

    // Gather MCP directory structure
    const mcpStructure = await gatherMCPStructure();

    const prompt = `
Please analyze the following Model Context Protocol (MCP) file structure for the BusBuddy project and provide recommendations for proper setup and organization:

## Current MCP Directory Structure:
${mcpStructure}

## Context:
- BusBuddy is a .NET 8.0 WPF application for school bus fleet management
- Uses Syncfusion WPF components for UI
- Integrates with Azure SQL Database
- Has PowerShell automation (bb-* commands)
- Multiple MCP servers for different functionalities

## Please analyze and recommend:

1. **File Organization**: Is the current structure logical and maintainable?
2. **Server Architecture**: Are we properly separating concerns between different MCP servers?
3. **Configuration Management**: How should we handle MCP configuration files (.mcp/server.json, .vscode/mcp.json)?
4. **Best Practices**: What MCP best practices should we follow for enterprise applications?
5. **Integration Patterns**: How to properly integrate MCP servers with VS Code and PowerShell workflows?
6. **Security Considerations**: How to securely manage API keys and sensitive configuration?
7. **Testing Strategy**: What's the best approach for testing MCP servers in development?
8. **Documentation Standards**: How should we document MCP tools and server capabilities?

Please provide specific, actionable recommendations with code examples where appropriate.
`;

    try {
        const response = await fetch("https://api.x.ai/v1/chat/completions", {
            method: "POST",
            headers: {
                Authorization: `Bearer ${apiKey}`,
                "Content-Type": "application/json",
            },
            body: JSON.stringify({
                model: "grok-4-0709",
                messages: [
                    {
                        role: "system",
                        content:
                            "You are an expert software architect specializing in Model Context Protocol (MCP), .NET development, VS Code extensions, and enterprise application architecture. Provide detailed, actionable guidance following Microsoft and MCP best practices.",
                    },
                    {
                        role: "user",
                        content: prompt,
                    },
                ],
                max_tokens: 10000,
                temperature: 0.3,
                stream: false,
            }),
        });

        console.log(
            `📡 Response Status: ${response.status} ${response.statusText}`,
        );

        if (!response.ok) {
            const errorData = await response.json().catch(() => ({}));
            console.error("❌ API Error:", errorData);
            return;
        }

        const data = await response.json();
        const analysis =
            data.choices?.[0]?.message?.content || "No response content";

        console.log("\n🤖 Grok-4 MCP Structure Analysis:");
        console.log("=".repeat(80));
        console.log(analysis);
        console.log("=".repeat(80));
        console.log(
            `📝 Finish Reason: ${data.choices?.[0]?.finish_reason || "Unknown"}`,
        );
        console.log(
            `🔢 Completion Tokens: ${data.usage?.completion_tokens || 0}`,
        );
        console.log(`🧠 Reasoning Tokens: ${data.usage?.prompt_tokens || 0}`);

        // Save analysis to file
        await fs.writeFile(
            "mcp-structure-analysis.md",
            `# BusBuddy MCP Structure Analysis\n\n${analysis}\n\n---\n*Generated by Grok-4 on ${new Date().toISOString()}*`,
        );
        console.log("\n💾 Analysis saved to: mcp-structure-analysis.md");
    } catch (error) {
        console.error("❌ Error analyzing MCP structure:", error.message);
    }
}

async function gatherMCPStructure() {
    const structure = [];

    try {
        // Read MCP directory
        const mcpDir = "./mcp";
        const items = await fs.readdir(mcpDir, { withFileTypes: true });

        structure.push("📁 mcp/");

        for (const item of items) {
            if (item.isDirectory()) {
                structure.push(`  📁 ${item.name}/`);

                // Get subdirectory contents
                try {
                    const subItems = await fs.readdir(
                        path.join(mcpDir, item.name),
                        { withFileTypes: true },
                    );
                    for (const subItem of subItems) {
                        const icon = subItem.isDirectory() ? "📁" : "📄";
                        structure.push(`    ${icon} ${subItem.name}`);
                    }
                } catch (err) {
                    structure.push(
                        `    ❌ Error reading ${item.name}: ${err.message}`,
                    );
                }
            } else {
                structure.push(`  📄 ${item.name}`);
            }
        }

        // Check for VS Code MCP config
        try {
            await fs.access(".vscode/mcp.json");
            structure.push("\n📁 .vscode/");
            structure.push("  📄 mcp.json");
        } catch {
            structure.push("\n❌ .vscode/mcp.json not found");
        }
    } catch (error) {
        structure.push(`❌ Error reading MCP structure: ${error.message}`);
    }

    return structure.join("\n");
}

analyzeMCPStructure();
